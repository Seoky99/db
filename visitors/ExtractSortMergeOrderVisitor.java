	@Override
	public void visit(EqualsTo arg0) {

		Expression left = arg0.getLeftExpression();
		Expression right = arg0.getRightExpression();

		//tableName.attribute
		String leftString = ((Column) left).toString();
		String rightString = ((Column) right).toString();

		String[] leftArray = leftString.split("\\.");
		String leftTable = leftArray[0];

		// expression on the left should go the the rightorderby
		if (leftTable.equals(rightTable)) {

			rightOrderBy.add(leftString);
			leftOrderBy.add(rightString);

		} else {
			rightOrderBy.add(rightString);
			leftOrderBy.add(leftString);
		}

	}

