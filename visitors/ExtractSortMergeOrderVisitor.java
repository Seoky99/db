	/**
	 * Visits an and expression and recurses 
	 */
	@Override
	public void visit(AndExpression arg0) {
		
		Expression left = arg0.getLeftExpression();
		Expression right = arg0.getRightExpression();

		ExtractSortMergeOrderVisitor leftVisitor = new ExtractSortMergeOrderVisitor(rightTable);
		ExtractSortMergeOrderVisitor rightVisitor = new ExtractSortMergeOrderVisitor(rightTable);

		left.accept(leftVisitor);
		right.accept(rightVisitor);

		ArrayList<String> leftVisitorLeft = leftVisitor.leftOrderBy;
		ArrayList<String> leftVisitorRight = leftVisitor.rightOrderBy;


		for (String s : rightVisitor.leftOrderBy) {
			leftVisitorLeft.add(s);
		}
		
		for (String s : rightVisitor.rightOrderBy) {
			leftVisitorRight.add(s);
		}
		
		this.leftOrderBy = leftVisitorLeft; 
		this.rightOrderBy = leftVisitorRight; 
		
	}

